#!/usr/bin/env python3

"""
    Concatenates two strings and returns the result.
"""


def concat(str1: str, str2: str) -> str:
    """
    Concatenates two strings and returns the result.

    :param str1: The first string.
    :param str2: The second string.
    :return: The concatenated string.
    """
    return str1 + str2
